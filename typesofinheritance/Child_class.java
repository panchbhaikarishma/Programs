package typesofinheritance;
class parent2
{
	
static void add()
{
	System.out.println("Adding two numbers");
	}

}

public class Child_class extends parent2
{
	static void sub()
	{
		System.out.println("Subracting two numbers");
	}
public static void main(String[] args) 
{
	
	add();
	sub();
	
	
	
}
}
